package bo.edu.ucb.ingsoft.movies.dto;

public class PremiosList {
    private String pelicula;
    private String cantidadPremiosOscar;

    public PremiosList(String pelicula, String cantidadPremiosOscar) {
        this.pelicula = pelicula;
        this.cantidadPremiosOscar = cantidadPremiosOscar;

    }

    public String getpelicula() {
        return pelicula;
    }

    public void setPelicula(String pelicula) {
        this.pelicula = pelicula;
    }

    public String getCantidadPremiosOscar() {
        return cantidadPremiosOscar;
    }

    public void setCantidadPremiosOscar(String cantidadPremiosOscar) {
        this.cantidadPremiosOscar = cantidadPremiosOscar;
    }


    @Override
    public String toString() {
        return "{" +
                "pelicula='" + pelicula + '\'' +
                ", Cantidad  de victorias en los premios Oscar='" + cantidadPremiosOscar + '\'' +
                '}';
    }
}
